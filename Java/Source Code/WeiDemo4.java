class WeiDemo4{
    public static void main(String[] args){
        // >>
        System.out.println(4>>2);//4/2^2
        System.out.println(-15>>3);
        // <<
        System.out.println(4<<2);//4*2^2
        //>>>
        System.out.println(4>>>2);
        System.out.println(1>>1);
        System.out.println(1>>32);
        System.out.println(1>>33);

    }
}
